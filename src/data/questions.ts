export function getRandomQuizQuestions(count: number = QUESTIONS_PER_ROUND): QuizQuestion[] {
  const shuffled = [...quizQuestions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

export function getQuestionsByDifficulty(difficulty: QuizQuestion['difficulty']): QuizQuestion[] {
  return quizQuestions.filter(q => q.difficulty === difficulty);
}

export function getRoundQuestions(round: number, count: number = QUESTIONS_PER_ROUND): QuizQuestion[] {
  const normalizedRound = Math.min(Math.max(round, 1), TOTAL_ROUNDS);
  const shuffled = [...quizQuestions].sort(() => Math.random() - 0.5);
  return shuffled.slice(0, count);
}

export function generateAllRounds(): QuizQuestion[][] {
  return Array.from({ length: TOTAL_ROUNDS }, (_, index) => getRoundQuestions(index + 1));
}